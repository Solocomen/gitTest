package epc.epcsalesapi.sales.bean;

import java.util.ArrayList;

/**
 *
 * @author DannyChan
 */
public class EpcProduct {
    private int id;
    private String name;
    private String guid;
    private ArrayList<String> productCodes;
    private String productType;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the guid
     */
    public String getGuid() {
        return guid;
    }

    /**
     * @param guid the guid to set
     */
    public void setGuid(String guid) {
        this.guid = guid;
    }

    /**
     * @return the productCodes
     */
    public ArrayList<String> getProductCodes() {
        return productCodes;
    }

    /**
     * @param productCodes the productCodes to set
     */
    public void setProductCodes(ArrayList<String> productCodes) {
        this.productCodes = productCodes;
    }

    /**
     * @return the productType
     */
    public String getProductType() {
        return productType;
    }

    /**
     * @param productType the productType to set
     */
    public void setProductType(String productType) {
        this.productType = productType;
    }
}
