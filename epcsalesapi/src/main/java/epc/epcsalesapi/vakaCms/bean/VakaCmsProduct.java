public class VakaCmsProduct {
    private String skuCode;
    private String productNameEng;
    private String productNameChi;

    public VakaCmsProduct() {}

    public String getSkuCode() {
        return skuCode;
    }

    public void setSkuCode(String skuCode) {
        this.skuCode = skuCode;
    }

    public String getProductNameEng() {
        return productNameEng;
    }

    public void setProductNameEng(String productNameEng) {
        this.productNameEng = productNameEng;
    }

    public String getProductNameChi() {
        return productNameChi;
    }

    public void setProductNameChi(String productNameChi) {
        this.productNameChi = productNameChi;
    }

    
}
